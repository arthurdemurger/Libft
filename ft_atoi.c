#include "libft.h"

int	ft_atoi(const char *str)
{
	unsigned long long	sum;
	int					sign;
	int					i;

	sum = 0;
	sign = 1;
	i = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		sum = sum * 10 + str[i++] - 48;
		if (sum > LLONG_MAX && sign == 1)
			return (-1);
		if (sum > LLONG_MAX && sign == -1)
			return (0);
	}
	return (sum * sign);
}
